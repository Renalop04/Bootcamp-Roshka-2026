package Objetos;

public class Aula {
    private int id_aula;
    private String nombre;

    public void Aula(){
        this.id_aula = id_aula;
        this.nombre = nombre;
    }

    public int getId_aula() {
        return id_aula;
    }

    public String getNombre() {
        return nombre;
    }
}
