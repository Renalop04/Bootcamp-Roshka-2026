package Objetos;

public class Libro {
    private int id_libro;
    private int id_editorial;
    private String titulo;

    public void Libro(){
        this.id_editorial = id_editorial;
        this.id_libro = id_libro;
        this.titulo = titulo;
    }

    public int getId_editorial() {
        return id_editorial;
    }

    public int getId_libro() {
        return id_libro;
    }

    public String getTitulo() {
        return titulo;
    }

}
