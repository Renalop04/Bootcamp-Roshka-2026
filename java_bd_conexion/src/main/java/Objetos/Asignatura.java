package Objetos;

public class Asignatura {
    private int id_asignatura;
    private String nombre;

    public void Asignatura(){
        this.id_asignatura = id_asignatura;
        this.nombre = nombre;
    }

    public int getId_asignatura() {
        return id_asignatura;
    }

    public String getNombre() {
        return nombre;
    }
}
